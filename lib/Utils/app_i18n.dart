extension AppI18nContext on BuildContext {
  String tr(String key) {
    final lang = watch<LanguageProvider>().language;
    return AppI18n.tr(key, lang);
  }

  String trError(String message) {
    final lang = watch<LanguageProvider>().language;
    return AppI18n.localizeError(message, lang);
  }
}
