  static String tr(String key, AppLanguage language) {
    final table = language == AppLanguage.chichewa ? _ny : _en;
    return table[key] ?? key;
  }

  static String localizeError(String message, AppLanguage language) {
    final raw = message.trim();
    final normalized = raw.startsWith('Exception: ')
        ? raw.substring('Exception: '.length)
        : raw;

    const exactMap = <String, String>{
      'Invalid phone number.': 'phone_invalid',
      'Could not continue right now.': 'could_not_continue',
      'Could not send OTP. Please try again.': 'could_not_send_otp',
      'Verification session expired. Request a new code.':
          'verification_session_expired',
      'Invalid code.': 'invalid_code',
      'Could not verify code.': 'could_not_verify_code',
      'Not signed in.': 'not_signed_in',
      'Could not save profile basics.': 'could_not_save_profile_basics',
      'You are offline. Profile changes were saved and will sync soon.':
          'profile_saved_sync_soon',
      'Backend session unavailable': 'backend_session_unavailable_plain',
      'Backend session unavailable.': 'backend_session_unavailable_punct',
      'Location permission is required for discovery':
          'location_permission_discovery',
      'Enable location permission to discover nearby people.':
          'enable_location_discovery',
      'Could not load nearby profiles.': 'could_not_load_nearby_profiles',
      'No internet right now. Like queued and will retry.': 'like_queued_retry',
      'You are offline. We will sync when back online.': 'offline_sync',
      'Notification permission denied.': 'notification_permission_denied',
      'Unable to unmatch right now. Please try again.': 'unable_to_unmatch_now',
    };

    final key = exactMap[normalized];
    if (key != null) return tr(key, language);

    if (normalized.startsWith('Backend session error:')) {
      final detail = normalized.substring('Backend session error:'.length).trim();
      return '${tr('backend_session_error_prefix', language)}: $detail';
    }
    if (normalized.startsWith('Could not update profile:')) {
      final detail = normalized.substring('Could not update profile:'.length).trim();
      return '${tr('could_not_update_profile_prefix', language)}: $detail';
    }
    if (normalized.startsWith('Could not send like:')) {
      final detail = normalized.substring('Could not send like:'.length).trim();
      return '${tr('could_not_send_like_prefix', language)}: $detail';
    }

    return normalized;
  }

  static bool isLocationError(String? message) {
    if (message == null) return false;
    return message.toLowerCase().contains('location');
  }
